
# exponential operator
print(2 ** 5)
print(2. ** 5)
print(2 ** 5.)
print(2. ** 5.)

# asterisk(*) multiplication operator
print(2 * 5)
print(2. * 5)
print(2 * 5.)
print(2. * 5.)

# Division operator
print(5 / 2)
print(5. / 2)
print(5 / 2.)
print(5. / 2.)

# floor division operator
print(5 // 2)  # will return near value and remove rounding
print(5. // 2)
print(5 // 2.)
print(5. // 2.)


# modulo operator
print(5 % 2)  # will give reminder value

# addition operator

print(5 + 2)

print(5. + 2)

# subtraction operator

print(5 - 2)

print(5. - 2)



